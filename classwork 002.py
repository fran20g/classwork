class calculator:
    def add(self,a,b):
        c=a+b
        print("Addition is {}".format(c))

    def sub(self,a,b):
        c=a-b
        print("subtraction is {}".format(c))

    def mult(self,a,b):
        c=a*b
        print("Multiplication is {}".format(c))

    def div(self,a,b):
        c=a/b
        print("Division is {}".format(c))

     

cl = calculator()
cl.sub(28,2)